    private void btnBuscarEstuainteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarEstuainteActionPerformed
        // TODO add your handling code here:
        try{
            if(tfcedulaestudiante.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Por favor digite la cedula "
                        + "del estudiante que desea buscar");
            }else{
                Estudiante estu= controlador.buscarEstudiante(tfcedulaestudiante.getText());
                if(estu==null){
                    JOptionPane.showMessageDialog(null, "No existe registro de dicho estudiante");
                }else{
                    tfnombreestudiante.setText(estu.getNombre()+ " "+ estu.getApellido());
                    tfcreditosregistradosestudiante.setText(""+ controlador.creditosDelEstudiante(estu));
                }
            }
        }catch (Exception exc) {
            
            exc.printStackTrace();
        }
    }//GEN-LAST:event_btnBuscarEstuainteActionPerformed

    private void btnBuscarAsignaturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarAsignaturaActionPerformed
        try{
        	if(tfcodigoasignatura.getText().isEmpty()){
        		JOptionPane.showMessageDialog(null, "Por favor digite el codigo de la asignatura" );
        		
        	}else{
        		String codigoAsi=tfcodigoasignatura.getText();
        		Asignatura asig= controlador.buscarAsignatura(codigoAsi);
        		if(asig==null){
        			JOptionPane.showMessageDialog(null, "No se encuentra ninguna asignatura con este codigo");
        		}else{
        			tfnombreasignatura.setText(asig.getCodigo());
        			 tfcreditosasignatura.setText(asig.getNumeroCreditos() + "");
                     tfTipoAsignatura.setText(asig.getTipo().toString());
                     List<Curso> cursos= controlador.listarCursosPorAsignatura(asig);
                     List<SesionCurso> sesiones= new ArrayList();
                     DefaultTableModel tabla= (DefaultTableModel) tablaHorario.getModel();
                     
                     tabla.setRowCount(0);
                     for (Curso curso : cursos) {
                    	 String horario="";
                    	 sesiones= curso.getSesiones();
                    	 for (SesionCurso sesion: sesiones){
                    		 horario +=" "+ sesion.getDia().toString()+":"+ sesion.getHoraInicio()+"_"+
                    	 sesion.getHoraFinal();
                    	 }
                    	 
                    	 tabla.addRow(new Object[]{curso.getCodigo(),horario});
                     }
        		}
        	}
        }catch (Exception exc) {
             exc.printStackTrace();
        }
    	
    	
    }//GEN-LAST:event_btnBuscarAsignaturaActionPerformed

