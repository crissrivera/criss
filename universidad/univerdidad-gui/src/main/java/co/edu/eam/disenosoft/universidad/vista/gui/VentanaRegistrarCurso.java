    private void btnRegistrarAsignatureaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarAsignatureaActionPerformed
        // TODO add your handling code here:
    	try{
    		Asignatura asignatura=controlador.buscarAsignatura(tfcodigoasignatura.getText());
    		Estudiante estudiante=controlador.buscarEstudiante(tfcedulaestudiante.getText());
    		if(asignatura == null && estudiante == null){
                JOptionPane.showMessageDialog(null, "Por favor primero busque la "
                        + "asignatura y el estudiante");
            } else{
                if(tablaHorario.getRowCount() == 0){
                    JOptionPane.showMessageDialog(null, "No hay ningún curso con "
                            + "esa asignatura");
                } else{
    		
                DefaultTableModel tabla = (DefaultTableModel) tablaHorario.getModel();
                int row = tablaHorario.getSelectedRow();
                String codigoCurso = (String) tabla.getValueAt(row, 0);
                Curso curso = controlador.buscarCurso(codigoCurso);
                controlador.crearCurso(curso, estudiante);
                JOptionPane.showMessageDialog(null, "El estudiante ha registrado"
                        + " el curso con exito");
                           
               }
           
             
                
            }
    	}   catch(ExcepcionNegocio ex){
    		JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    	     
        } catch (Exception exc) {
              exc.printStackTrace();
        }
    	
    }//GEN-LAST:event_btnRegistrarAsignatureaActionPerformed

