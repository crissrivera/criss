    public int calculoEdad(Estudiante estudiante){
        int edad=0;
        int anio= estudiante.getFechaNacimiento().getYear()+1900;
        int mes= estudiante.getFechaNacimiento().getMonth();
        int dia= estudiante.getFechaNacimiento().getDate();
        Calendar fecha= new GregorianCalendar();
        int anioActual= fecha.get(Calendar.YEAR);
        int mesActual= fecha.get(Calendar.MONTH);
        int diaActual= fecha.get(Calendar.DAY_OF_MONTH);
        int  anioo= anioActual-anio;
        int mess= mesActual-mes;
        int diaa= diaActual- dia;
        if(mess<0 || (mes==0 && diaa<0)){
            edad= anioo-1;
        }else{
            edad=anioo*-1;
        }
        return edad;
        
        
    }

