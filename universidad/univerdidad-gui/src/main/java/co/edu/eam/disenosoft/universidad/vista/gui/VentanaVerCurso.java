    private void btnBuscarAsignaturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarAsignaturaActionPerformed
        // TODO add your handling code here:
        try {
            if (tfcodigoasignatura.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Porfavor digitar el código de "
                        + "la asignatura");
            } else {
                Asignatura asignatura = controlador.buscarAsignatura(tfcodigoasignatura.getText());
                if (asignatura == null) {
                    JOptionPane.showMessageDialog(null, "No se encontro ninguna "
                            + "asignatura");

                } else {
                    tfnombreasignatura.setText(asignatura.getNombre());
                    List<Curso> cursoo;
                    cursoo = controlador.listarCursosPorAsignatura(asignatura);
                    if (cursoo == null) {
                        JOptionPane.showMessageDialog(null, "La asignatura no tiene cursos");
                    } else {
                    	cargarComboCursos(cursoo);
                    }
                }
            }
        } catch (Exception e1) {
            e1.printStackTrace();

        }

    }//GEN-LAST:event_btnBuscarAsignaturaActionPerformed

    private void comboCursosItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboCursosItemStateChanged
        // TODO add your handling code here:
        try{
            DefaultTableModel tab= (DefaultTableModel) jTable1.getModel();
            if(comboCursos.getSelectedIndex()!=0){
                String codigoCurso=(String) comboCursos.getSelectedItem();
                if(codigoCurso==null){
                    return;
                }
                
                Curso curso= controlador.buscarCurso(codigoCurso);
                List<RegistroCurso> lista= controlador.listartodoslosregistrodecursoEstu(curso);
                tab.setRowCount(0);
                if(lista !=null){
                    for(RegistroCurso rC: lista){
                        Estudiante estu= rC.getEstudiante();
                        int edad= calculoEdad(estu);
                        tab.addRow(new Object[]{estu.getCedula(), estu.getNombre(), estu.getApellido(),edad});
                    }
                }
            }
           
        }catch (Exception exc) {
            
            exc.printStackTrace();
        }
    }//GEN-LAST:event_comboCursosItemStateChanged

    public void cargarComboCursos(List<Curso> cursos) {
        comboCursos.removeAllItems();
        comboCursos.addItem("Seleccione un curso");
        for (int i = 0; i < cursos.size(); i++) {
            comboCursos.addItem(cursos.get(i).getCodigo());
        }
    }

